
package l3hass1.L3HASS1;


public class Marks {
    
    public int m1,m2,m3;
    


public Marks() {
    m1 = 25; m2 =25; m3 = 25;

}

public Marks (int M1, int M2, int M3) {

M1 = m1;  M2 = m2; M3= m3;


}

public int calcaulatesum() {
    
    return(m1 + m2 + m3);
}
    
}
